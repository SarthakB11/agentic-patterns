"""Run every benchmark and regenerate the machine summary from measured numbers.

    python3 -m benchmarks.run_all          # free mock dry run of all benchmarks
    python3 -m benchmarks.run_all --live    # real run against Gemini (needs GEMINI_KEY)

The live run writes `benchmarks/results/SUMMARY.md` straight from the returned
`BenchResult` objects, so that table cannot drift from what was measured. The
run is cached, so re-running is nearly free and reproduces the same numbers.
The written report with interpretation is the hand-authored top-level RESULTS.md.
"""

from __future__ import annotations

import argparse
import importlib
import os
import pathlib

from benchmarks.harness import BenchResult

BENCHES = ["rag", "reflection", "routing", "react", "evaluation"]
# The runner writes a machine-generated table that always matches the data.
# The curated, prose report lives in the top-level RESULTS.md, hand-authored
# from these same numbers, and is not overwritten by a run.
SUMMARY_MD = pathlib.Path(__file__).resolve().parent / "results" / "SUMMARY.md"

PREAMBLE = """# Benchmark summary (generated)

This table is regenerated by `python3 -m benchmarks.run_all --live` and always
matches the numbers in `benchmarks/results/*.json`. For the written report and
what each result means, see the top-level `RESULTS.md`.

All numbers come from the repo's own pattern code run against Google Gemini 3.1
Flash-Lite through its OpenAI-compatible endpoint, with no change to
`agentic_patterns`. Task sets are small and purpose-built, N is stated per
benchmark, and every per-task outcome is recorded under `benchmarks/results/`.
"""


def _table(results: list[BenchResult]) -> str:
    rows = ["| Benchmark | N | Variants (headline metric) | Finding |", "|---|---|---|---|"]
    for r in results:
        variants = ", ".join(f"{k} {v:.2f}" for k, v in r.variants.items())
        rows.append(f"| {r.name} | {r.n} | {variants} | {r.headline} |")
    return "\n".join(rows)


def _sections(results: list[BenchResult]) -> str:
    out = []
    for r in results:
        cost = r.usage.get("cost_usd", 0.0)
        model = r.usage.get("model", r.model)
        out.append(f"## {r.name}\n")
        out.append(f"{r.headline}\n")
        out.append(f"- Model: {model}")
        out.append(f"- Tasks: {r.n}")
        out.append("- Metrics: " + ", ".join(f"{k} = {v:.3f}" for k, v in r.variants.items()))
        if r.detail:
            keep = {k: v for k, v in r.detail.items() if not isinstance(v, list)}
            if keep:
                out.append(f"- Detail: {keep}")
        out.append(f"- Cost this benchmark: ${cost:.4f}")
        out.append(f"- Provenance: `benchmarks/results/{r.name}.json`\n")
    return "\n".join(out)


def write_results_md(results: list[BenchResult], total_cost: float) -> None:
    body = [
        PREAMBLE,
        f"Total measured cost for the full live sweep: **${total_cost:.4f}** "
        f"across {sum(r.usage.get('api_calls', 0) for r in results)} model calls.\n",
        "## Summary\n",
        _table(results),
        "",
        _sections(results),
    ]
    # A second model's run (BENCH_RESULTS_SUBDIR set) writes its summary into
    # the same subfolder as its results, rather than overwriting the first.
    subdir = os.environ.get("BENCH_RESULTS_SUBDIR", "")
    summary_path = SUMMARY_MD.parent / subdir / "SUMMARY.md" if subdir else SUMMARY_MD
    summary_path.parent.mkdir(parents=True, exist_ok=True)
    summary_path.write_text("\n".join(body))


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--live", action="store_true", help="run against Gemini instead of the free mock path")
    args = parser.parse_args()

    results: list[BenchResult] = []
    total = 0.0
    for name in BENCHES:
        module = importlib.import_module(f"benchmarks.bench_{name}")
        result = module.run_live() if args.live else module.run_mock()
        results.append(result)
        total += result.usage.get("cost_usd", 0.0)
        headline = ", ".join(f"{k}={v:.3f}" for k, v in result.variants.items())
        print(f"{name:12} n={result.n:<3} {headline}  (${result.usage.get('cost_usd', 0.0):.4f})")

    if args.live:
        write_results_md(results, total)
        subdir = os.environ.get("BENCH_RESULTS_SUBDIR", "")
        loc = f"benchmarks/results/{subdir + '/' if subdir else ''}SUMMARY.md"
        print(f"\nWrote {loc}. Total live cost: ${total:.4f}")
    else:
        print("\nMock dry run complete (no cost). Use --live to measure and write RESULTS.md.")


if __name__ == "__main__":
    main()
